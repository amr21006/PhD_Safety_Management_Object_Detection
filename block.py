class CAM(nn.Module):
    def __init__(self, inc, c2, fusion='weight'):
        super().__init__()
        
        assert fusion in ['weight', 'adaptive', 'concat']
        self.fusion = fusion
        
        self.conv1 = DSConv2D(inc, inc, 3, 1, None, 1, d=1)
        self.conv2 = DSConv2D(inc, inc, 3, 1, None, 1, d=3)
        self.conv3 = DSConv2D(inc, inc, 3, 1, None, 1, d=5)
        
        self.fusion_1 = DSConv2D(inc, c2, 3)
        self.fusion_2 = DSConv2D(inc, c2, 3)
        self.fusion_3 = DSConv2D(inc, c2, 3)

        if self.fusion == 'adaptive':
            self.fusion_4 = DSConv2D(inc * 3, 3, 1)
    
    def forward(self, x):
        x1 = self.conv1(x)
        x2 = self.conv2(x)
        x3 = self.conv3(x)
        
        if self.fusion == 'weight':
            return self.fusion_1(x1) + self.fusion_2(x2) + self.fusion_3(x3)
        elif self.fusion == 'adaptive':
            fusion = torch.softmax(self.fusion_4(torch.cat([self.fusion_1(x1), self.fusion_2(x2), self.fusion_3(x3)], dim=1)), dim=1)
            x1_weight, x2_weight, x3_weight = torch.split(fusion, [1, 1, 1], dim=1)
            return x1 * x1_weight + x2 * x2_weight + x3 * x3_weight
        else:
            return torch.cat([self.fusion_1(x1), self.fusion_2(x2), self.fusion_3(x3)], dim=1)

